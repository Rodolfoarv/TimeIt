@property UIButton *signUpButton;



@end

@implementation ABCIntroView

- (instancetype)initWithFrame:(CGRect)frame{
    self = [super initWithFrame:frame];
    if(self){
        
        UIImageView *backgroundImageView = [[UIImageView alloc] initWithFrame:self.frame];
        backgroundImageView.image = [UIImage imageNamed:@"Intro_Screen_Background"];
        [self addSubview:backgroundImageView];
        
        self.scrollView = [[UIScrollView alloc] initWithFrame:self.frame];
        self.scrollView.pagingEnabled = YES;
        [self addSubview:self.scrollView];
        
        self.pageControl = [[UIPageControl alloc] initWithFrame:CGRectMake(0, self.frame.size.height*.8, self.frame.size.width, 10)];
        self.pageControl.currentPageIndicatorTintColor = [UIColor colorWithRed:0.153 green:0.533 blue:0.796 alpha:1.000];
        [self addSubview:self.pageControl];
    
        [self createViewOne];
        [self createViewTwo];
        [self createViewThree];
        [self createViewFour];
        
        
        //LogInButton
        self.logInButton = [[UIButton alloc] initWithFrame:CGRectMake(self.frame.size.width*.53, self.frame.size.height*.85, self.frame.size.width*.48, 60)];
        [self.logInButton setTintColor:[UIColor whiteColor]];
        [self.logInButton setTitle:@"Log In" forState:UIControlStateNormal];
        [self.logInButton.titleLabel setFont:[UIFont fontWithName:@"HelveticaNeue-Thin" size:18.0]];
        self.logInButton.backgroundColor = [UIColor colorWithRed:0.153 green:0.533 blue:0.796 alpha:1.000];
        self.logInButton.layer.borderColor = [UIColor colorWithRed:0.153 green:0.533 blue:0.796 alpha:1.000].CGColor;
        [self.logInButton addTarget:self action:@selector(onLogInButtonPressed:) forControlEvents:UIControlEventTouchUpInside];
        self.logInButton.layer.borderWidth =.5;
        self.logInButton.layer.cornerRadius = 15;
        [self addSubview:self.logInButton];
        
        
        //SignUpButton
        self.signUpButton = [[UIButton alloc] initWithFrame:CGRectMake(self.frame.size.width*.02, self.frame.size.height*.85, self.frame.size.width*.48, 60)];
        [self.signUpButton setTintColor:[UIColor whiteColor]];
        [self.signUpButton setTitle:@"SignUp" forState:UIControlStateNormal];
        [self.signUpButton.titleLabel setFont:[UIFont fontWithName:@"HelveticaNeue-Thin" size:18.0]];
        self.signUpButton.backgroundColor = [UIColor colorWithRed:0 green:0.533 blue:0 alpha:1.000];
        self.signUpButton.layer.borderColor = [UIColor colorWithRed:0.153 green:0.533 blue:0.796 alpha:1.000].CGColor;
        [self.signUpButton addTarget:self action:@selector(onSignUpButtonPressed:) forControlEvents:UIControlEventTouchUpInside];
        self.signUpButton.layer.borderWidth =.5;
        self.signUpButton.layer.cornerRadius = 15;
        [self addSubview:self.signUpButton];
        
        
        self.pageControl.numberOfPages = 4;
        self.scrollView.contentSize = CGSizeMake(self.frame.size.width*4, self.scrollView.frame.size.height);
        
        //This is the starting point of the ScrollView
        CGPoint scrollPoint = CGPointMake(0, 0);
        [self.scrollView setContentOffset:scrollPoint animated:YES];
    }
    return self;
}

- (void)onLogInButtonPressed:(id)sender {
    [self.delegate onLogInButtonPressed];
}

- (void)onSignUpButtonPressed:(id)sender {
    [self.delegate onSignUpButtonPressed];
}

- (void)scrollViewDidScroll:(UIScrollView *)scrollView {
    
    CGFloat pageWidth = CGRectGetWidth(self.bounds);
    CGFloat pageFraction = self.scrollView.contentOffset.x / pageWidth;
    self.pageControl.currentPage = roundf(pageFraction);
    
}


-(void)createViewOne{
    
